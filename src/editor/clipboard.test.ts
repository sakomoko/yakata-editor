describe('computeBoundingBox', () => {
  it('computes BB for rooms', () => {
    const rooms = [makeRoom({ x: 2, y: 3, w: 4, h: 3 })];
    const bb = _computeBoundingBox(ents(rooms));
    expect(bb.minGx).toBe(2);
    expect(bb.minGy).toBe(3);
    expect(bb.maxGx).toBe(6);
    expect(bb.maxGy).toBe(6);
  });

  it('computes BB for polygon rooms', () => {
    const v: [GridPoint, GridPoint, GridPoint, GridPoint] = [
      { gx: 1, gy: 2 },
      { gx: 5, gy: 2 },
      { gx: 6, gy: 4 },
      { gx: 0, gy: 4 },
    ];
    const rooms = [makeRoom({ vertices: v })];
    const bb = _computeBoundingBox(ents(rooms));
    expect(bb.minGx).toBe(0);
    expect(bb.minGy).toBe(2);
    expect(bb.maxGx).toBe(6);
    expect(bb.maxGy).toBe(4);
  });
});

describe('paste with mirror', () => {
  it('horizontal mirror paste flips room position', () => {
    const room = makeRoom({ x: 0, y: 0, w: 2, h: 2 });
    const ec = makeEc([room], [], [], new Set([room.id]));
    copySelection(ec);

    ec.state.mouse = { px: 0, py: 0, gx: 0, gy: 0 };
    pasteClipboard(ec, 'horizontal');

    expect(ec.state.rooms).toHaveLength(2);
    const pasted = ec.state.rooms[1];
    // BB: originGx=0, bbW=2, centerGx=1
    // mirror: x = 2*1 - 0 - 2 = 0 (symmetric)
    expect(pasted.x).toBe(0);
  });

  it('vertical mirror paste flips room position', () => {
    const room = makeRoom({ x: 0, y: 0, w: 4, h: 2 });
    const ec = makeEc([room], [], [], new Set([room.id]));
    copySelection(ec);

    ec.state.mouse = { px: 0, py: 0, gx: 0, gy: 0 };
    pasteClipboard(ec, 'vertical');

    expect(ec.state.rooms).toHaveLength(2);
  });
});
